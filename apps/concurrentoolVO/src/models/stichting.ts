/**
 * Stichting (bestuur) — first-class entity introduced in Phase 27 Plan 02.
 *
 * A Stichting groups N schools under a single bestuur. Sales-accountmanagers
 * use this for cross-school planning, bulk-export (Plan 11) and bulk-link
 * (Plan 07). The `usageMix` indicator on the overview card is derived from
 * each school's `currentToolUsage` (filled by Plan 27-05) — until that lands
 * the helper returns `'unknown'`.
 */

import type { SchoolRecord } from '@/db/types';

/**
 * Authoritative Stichting record shape. Mirrored 1-on-1 between Supabase
 * (`stichtingen` table, Migration 014) and Dexie v3 (`stichtingen` store).
 *
 * Dates are kept as ISO strings to match the rest of the persistence layer
 * (see `SchoolRecord` — strings, not `Date` objects).
 */
export interface StichtingRecord {
  /** UUID generated by Supabase (`gen_random_uuid()`). */
  id: string;
  /** Owning team (RLS scope). */
  teamId: string;
  /** Stichting name (e.g. "Stichting Voortgezet Onderwijs Amsterdam"). */
  name: string;
  /** Optional region label — defaults to '' if not provided. */
  region: string;
  /** ISO timestamp from Supabase. */
  createdAt: string;
  /** ISO timestamp from Supabase (bumped via `set_updated_at` trigger). */
  updatedAt: string;
  /** UUID of the user who created the row. */
  createdBy?: string;
  /** UUID of the user who last modified the row. */
  updatedBy?: string;
}

/**
 * Aggregated usage mix across all schools linked to a Stichting.
 *
 * - `cito-only`     — every linked school is fully on Cito.
 * - `concurrent-only` — every linked school uses only competitors.
 * - `mixed`         — at least one Cito school and at least one competitor school.
 * - `unknown`       — no linked schools, or the `currentToolUsage` data
 *                     hasn't been collected yet (pre Plan 27-05).
 */
export type UsageMix = 'cito-only' | 'concurrent-only' | 'mixed' | 'unknown';

