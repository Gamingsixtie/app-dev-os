/**
 * Derive the usage-mix for a Stichting from its linked schools.
 *
 * Plan 27-05 (R5) flips this from the Plan 27-02 placeholder to real
 * classification based on each school's per-niveau `currentToolUsage`
 * map. Aggregation rules (per Plan 27-05 interfaces-block):
 *
 * - Any per-niveau `'mix'` value, or both Cito- and concurrent-side
 *   values across the corpus → `'mixed'`.
 * - Every recorded value === `'cito'` → `'cito-only'`.
 * - Every recorded value ∈ `{'dia','jij','geen'}` (no Cito anywhere)
 *   → `'concurrent-only'`. (`'geen'` = "nieuwe markt" — treated as
 *   non-Cito side since the school is not on Cito today.)
 * - No recorded values at all (legacy schools / fresh stichting)
 *   → `'unknown'`.
 *
 * @param schools The schools linked to the Stichting. Pass an empty array
 * if the Stichting has no schools yet (also returns `'unknown'`).
 */
export function getStichtingUsageMix(schools: SchoolRecord[]): UsageMix {
  if (!schools || schools.length === 0) return 'unknown';

  let hasCito = false;
  let hasConcurrent = false;
  let hasAnyValue = false;

  for (const school of schools) {
    const usage = school.currentToolUsage;
    if (!usage) continue;
    for (const value of Object.values(usage)) {
      if (!value) continue;
      hasAnyValue = true;
      // Per-niveau 'mix' immediately implies the stichting is mixed —
      // can short-circuit but keep iterating cheap for clarity.
      if (value === 'mix') {
        return 'mixed';
      }
      if (value === 'cito') {
        hasCito = true;
      } else {
        // 'dia' | 'jij' | 'geen' — all non-Cito-side
        hasConcurrent = true;
      }
      if (hasCito && hasConcurrent) {
        return 'mixed';
      }
    }
  }

  if (!hasAnyValue) return 'unknown';
  if (hasCito && !hasConcurrent) return 'cito-only';
  if (hasConcurrent && !hasCito) return 'concurrent-only';
  return 'mixed';
}

/**
 * Error thrown by `deleteStichting` when the Stichting still has linked
 * schools. Cascade-guard per D-04: sales must explicitly unlink schools
 * before the bestuur can be removed (prevents accidental data loss).
 */
export class StichtingCascadeError extends Error {
  readonly stichtingId: string;
  readonly linkedSchoolCount: number;

  constructor(stichtingId: string, linkedSchoolCount: number) {
    const schoolWord = linkedSchoolCount === 1 ? 'school' : 'scholen';
    super(
      `Stichting ${stichtingId} kan niet verwijderd worden: ${linkedSchoolCount} ${schoolWord} nog gekoppeld.`,
    );
    this.name = 'StichtingCascadeError';
    this.stichtingId = stichtingId;
    this.linkedSchoolCount = linkedSchoolCount;
  }
}
